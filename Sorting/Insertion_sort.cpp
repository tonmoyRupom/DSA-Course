#include<bits/stdc++.h>
#define ll long long
using namespace std;

void insertion_sort(int a[],int n)
{
  for(int i=1;i<n;i++)
  {
    int key=a[i];
    int j=i-1;
    while(a[j]>key && j>=0)
    {
      a[j+1]=a[j];
      j--;
    }
    a[j+1]=key;
  }
}

void print(int a[],int n)
{
  for(int i=0;i<n;i++)
  {
    cout<<a[i]<<" ";
  }
}


